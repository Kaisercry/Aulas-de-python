def areaQuadrado(lado):
    area = lado*lado
    return area

def areaRetangulo(base,altura):
    area = base*altura
    return area

def areaTriangulo(base,altura):
    area = base*altura/2
    return area

def areaTrapezio(basemaior,basemenor,altura):
    area = (basemaior + basemenor)*altura/2
    return area